using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public enum EquipmentSlot
{
    /// <summary>
    /// Attatched to the head as a hat-like item.
    /// For example, a baseball cap, a bandana, or a helmet.
    /// </summary>
    HEAD_HAT,
    /// <summary>
    /// Attatched to the head via the face. 
    /// For example, a hockey mask.
    /// </summary>
    HEAD_FACE,
    /// <summary>
    /// Attatched to the head, covering the eyes.
    /// For example, goggle, or glasses.
    /// </summary>
    HEAD_FACE_EYES,
    /// <summary>
    /// Attatched to the head on the lower part of the face.
    /// For example, a bandana covering covering the nose and hanging past the chin
    /// </summary>
    HEAD_FACE_LOWER,
    /// <summary>
    /// Attatched to the head, but covering it completely. 
    /// For example, a riot helmet with a face shield. 
    /// </summary>
    HEAD_FULL,
    /// <summary>
    /// 
    /// </summary>
    TORSO,

    TORSO_OVER,

}
